let jaFoiHalloween = ['silhueta', 'cor', 'musica', 'personagem', 'endereco', 'genero', 'aniversario'];

function verificarRespostaHalloween(numeroDiario, modo, numImg) {
    let res = halloLunos[numeroDiario]

    const inputAluno = document.getElementById("inAluno");

    if (jaFoiHalloween.length == 0) {
        // console.log(document.getElementById("foto"))
        document.getElementById("titulo").innerHTML = 'VOCÊ PERDEU!'
        document.getElementById("foto").innerHTML = `
            <img src="/assets/img/halloween/aluno${dadosHalloween[res][6]}.png">
            `;

        document.getElementById('button-section').innerHTML = `
            <button onclick="resetar()">Voltar</button>
        `

        document.getElementById('inAluno').style.display = 'none'
    }

    if (res === inputAluno.value) {
        document.getElementById("titulo").innerHTML = 'VOCÊ ACERTOU!'
        document.getElementById("foto").innerHTML = `
            <img src="/assets/img/halloween/aluno${dadosHalloween[res][6]}.png">
            `;

        document.getElementById('button-section').innerHTML = `
            <button onclick="resetar()">Voltar</button>    
        `

        document.getElementById('inAluno').style.display = 'none'

        document.getElementById("foto").style.backgroundColor = "transparent";

        confetti({
            particleCount: 200, // quantidade de confetes
            spread: 100,        // abertura do efeito
            origin: { y: 0.6 }  // altura inicial (0 = topo, 1 = base)
        });
    } else if (jaFoiHalloween.length !== 0) {
        let escolhido = Math.floor(Math.random() * jaFoiHalloween.length);

        // console.log(jaFoiHalloween[escolhido]);

        if (jaFoiHalloween[escolhido] == 'silhueta') {
            document.getElementById("foto").style.backgroundColor = "transparent";
            document.getElementById('foto').innerHTML = `
                <img src="/assets/img/silhueta/halloween/aluno${numImg}.png">
            `

            // 0: genero || 1: aniversario || 2: bairro e cidade || 3: cor || 4: personagem || 5: musica
        } else if (jaFoiHalloween[escolhido] == 'cor') {
            let body = document.querySelector('body')

            // console.log(dadosHalloween[res][3]);

            if (dadosHalloween[res][3] == 'arco-iris') {
                body.style.background = `linear-gradient(to right, red, orange, yellow, green, aqua, blue, purple)`
            } else {
                body.style.backgroundColor = dadosHalloween[res][3];
            }

        } else if (jaFoiHalloween[escolhido] == 'musica') {
            if (Math.random() < 0.5) {
                random1Halloween.innerHTML += '<hr>'
                random1Halloween.innerHTML += `<h3>Música Favorita:</h3> ${dadosHalloween[res][5]}`;
            } else {
                random2Halloween.innerHTML += '<hr>'
                random2Halloween.innerHTML += `<h3>Música Favorita:</h3> ${dadosHalloween[res][5]}`;
            }
        } else if (jaFoiHalloween[escolhido] == 'personagem') {
            let campo = ''
            let person = dadosHalloween[res][4]

            for (let i = 0; i < person.length; i++) {
                if (person[i] == ' ') {
                    campo += ' ';
                } else {
                    campo += '_';
                }
            }

            if (Math.random() < 0.5) {
                random1Halloween.innerHTML += '<hr>'
                random1Halloween.innerHTML += `<h3>Caracteres do Personagem:</h3> ${campo}`;
            } else {
                random2Halloween.innerHTML += '<hr>'
                random2Halloween.innerHTML += `<h3>Caracteres do Personagem:</h3> ${campo}`;
            }
        } else if (jaFoiHalloween[escolhido] == 'endereco') {
            if (Math.random() < 0.5) {
                random1Halloween.innerHTML += '<hr>'
                random1Halloween.innerHTML += `<h3>Local de Moradia:</h3> ${dadosHalloween[res][2]}`;
            } else {
                random2Halloween.innerHTML += '<hr>'
                random2Halloween.innerHTML += `<h3>Local de Moradia:</h3> ${dadosHalloween[res][2]}`;
            }
        } else if (jaFoiHalloween[escolhido] == 'genero') {
            if (Math.random() < 0.5) {
                random1Halloween.innerHTML += '<hr>'
                random1Halloween.innerHTML += `<h3>Gênero: ${dadosHalloween[res][0]}</h3>`;
            } else {
                random2Halloween.innerHTML += '<hr>'
                random2Halloween.innerHTML += `<h3>Gênero: ${dadosHalloween[res][0]}</h3>`;
            }
        } else if (jaFoiHalloween[escolhido] == 'aniversario') {
            if (Math.random() < 0.5) {
                random1Halloween.innerHTML += '<hr>'
                random1Halloween.innerHTML += `<h3>${dadosHalloween[res][1]}</h3>`;
            } else {
                random2Halloween.innerHTML += '<hr>'
                random2Halloween.innerHTML += `<h3>${dadosHalloween[res][1]}</h3>`;
            }
        }

        // remove o item ja escolhido do array para não repetir
        jaFoiHalloween.splice(escolhido, 1);
        inputAluno.value = ''
    }
}

